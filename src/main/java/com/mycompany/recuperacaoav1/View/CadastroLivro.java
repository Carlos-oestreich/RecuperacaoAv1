    private void btnSalvarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSalvarMouseClicked
        if (txtNomeDoLivro.getText().trim().isEmpty() || txtEditora.getText().trim().isEmpty() || txtAno.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Todos os campos devem ser preenchidos.", "Atenção", JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {
            Livro livro = new Livro();
            livro.setTitulo(txtNomeDoLivro.getText());
            livro.setEditora(txtEditora.getText());
            livro.setAno(txtAno.getText());
            livro.setDisponivel(rbtSim.isSelected());

            LivroDAO livroDAO = new LivroDAO();

            if (idLivroSelecionado == -1) { 
                livroDAO.salvarLivro(livro);
                JOptionPane.showMessageDialog(this, "Livro salvo com sucesso!");
            } else { 
                livro.setId(idLivroSelecionado);
                livroDAO.editarLivro(livro);
                JOptionPane.showMessageDialog(this, "Livro editado com sucesso!");
            }

            limparCampos();
            atualizarTabela(); 

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao salvar livro: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSalvarMouseClicked

