    private void btnExcluirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnExcluirMouseClicked
        int selectedRow = tblLivros.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um livro na tabela para excluir.", "Atenção", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int livroId = (int) tblLivros.getValueAt(selectedRow, 0);
        int confirm = JOptionPane.showConfirmDialog(this, "Tem certeza que deseja excluir este livro?", "Confirmar Exclusão", JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            try {
                LivroDAO livroDAO = new LivroDAO();
                livroDAO.excluirLivro(livroId);
                JOptionPane.showMessageDialog(this, "Livro excluído com sucesso!");
                atualizarTabela(); 
                limparCampos();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Erro ao excluir livro: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnExcluirMouseClicked

    private void tblLivrosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblLivrosMouseClicked
        int selectedRow = tblLivros.getSelectedRow();
        if (selectedRow != -1) {
            idLivroSelecionado = (int) tblLivros.getValueAt(selectedRow, 0);
            txtNomeDoLivro.setText(tblLivros.getValueAt(selectedRow, 1).toString());
            txtEditora.setText(tblLivros.getValueAt(selectedRow, 2).toString());
            txtAno.setText(tblLivros.getValueAt(selectedRow, 3).toString());

            String disponivel = tblLivros.getValueAt(selectedRow, 4).toString();
            if (disponivel.equals("Sim")) {
                rbtSim.setSelected(true);
            } else {
                rbtNao.setSelected(true);
            }
        }
    }//GEN-LAST:event_tblLivrosMouseClicked

