private void atualizarTabela() {
        DefaultTableModel modelo = (DefaultTableModel) tblLivros.getModel();
        modelo.setNumRows(0); // Limpa a tabela antes de carregar novos dados

        try {
            ArrayList<Livro> livros = LivroDAO.retornarListaDeLivros();
            for (Livro l : livros) {
                // Adiciona o ano na tabela
                Object[] linha = {
                    l.getId(),
                    l.getTitulo(),
                    l.getEditora(),
                    l.getAno(), // Verifique se sua JTable no modo Design tem 4 ou 5 colunas. Adicione uma para o Ano se necessário.
                    l.isDisponivel() ? "Sim" : "Não"
                };
                modelo.addRow(linha);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Erro ao carregar livros da base de dados: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }

private void limparCampos() {
        txtNomeDoLivro.setText("");
        txtEditora.setText("");
        txtAno.setText("");
        rbtSim.setSelected(true);
        idLivroSelecionado = -1; // Reseta o ID selecionado
        tblLivros.clearSelection(); // Limpa a seleção da tabela
    }



